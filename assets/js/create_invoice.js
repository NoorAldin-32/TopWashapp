function initmaps(position) {
    // Get location data
    lat = position.coords.latitude;
    long = position.coords.longitude;
    var $deflat = $('input[name=lat_s').val(lat) ? parseFloat($("input[name=lat_s]").val()) : 0;
    var $deflng = $('input[name=long_s').val(long) ? parseFloat($("input[name=long_s]").val()) : 0;


    map = new google.maps.Map(document.getElementById('dvMaps'), {
        streetViewControl: false,
        mapTypeControl: false,
        center: { lat: $deflat, lng: $deflng },
        zoom: 16
    });
    var pos = {
        lat: $deflat,
        lng: $deflng
    }
    const locationButton = document.createElement("button");
    service = new google.maps.places.PlacesService(map);
    locationButton.innerHTML = '<i class="fa-solid fa-location-crosshairs"></i>';
    locationButton.title = "My current location";
    locationButton.classList.add("btn");
    locationButton.classList.add("btn-yellow");
    locationButton.classList.add("btn-icon");
    locationButton.classList.add("custom-map-control-button");
    locationButton.addEventListener("click", (event) => {
        event.preventDefault();

        if (navigator.geolocation) {

            navigator.geolocation.getCurrentPosition(function (position) {
                var pos = {
                    lat: position.coords.latitude,
                    lng: position.coords.longitude
                };

                $("input[name=lat_s]").val(pos.lat);
                $("input[name=long_s]").val(pos.lng);
                marker.setPosition(pos);
                map.setCenter(pos);
            }, function () {
                handleLocationError(true, infoWindow, map.getCenter());
            });
        }
        else {
            // Browser doesn't support Geolocation
            handleLocationError(false, infoWindow, map.getCenter());
        }
    });
    map.controls[google.maps.ControlPosition.BOTTOM_LEFT].push(locationButton);

    marker = new google.maps.Marker({
        position: pos,
        map,
        draggable: true,
        animation: google.maps.Animation.DROP,
        title: "Your location",
    });
    map.addListener("click", function (event) {
        var lat = event.latLng.lat();
        var lng = event.latLng.lng();
        var posnew = {
            lat: lat,
            lng: lng
        };
        marker.setPosition(posnew);
        $("input[name=lat_s]").val(lat);
        $("input[name=long_s]").val(lng);
    });
    marker.addListener("dragend", function (event) {
        var lat = event.latLng.lat();
        var lng = event.latLng.lng();
        $("input[name=lat_s]").val(lat);
        $("input[name=long_s]").val(lng);
    });

    map.setCenter(pos);
    // Try HTML5 geolocation.
    if (lat == 0 || long == 0) {
        locationButton.click();
    }
    const searchInput = document.createElement("input");
    searchInput.placeholder = "type location to search";
    searchInput.classList.add('form-control');
    searchInput.classList.add('col-md-6');
    searchInput.addEventListener("keypress", function (event) {
        // If the user presses the "Enter" key on the keyboard
        if (event.key === "Enter") {
            // Cancel the default action, if needed
            event.preventDefault();
            // Trigger the button element with a click
            if ($(this).val()) {
                var request = {
                    query: $(this).val(),
                    fields: ['geometry']
                };
                service.findPlaceFromQuery(request, function (results, status) {
                    if (status === google.maps.places.PlacesServiceStatus.OK) {
                        if (results.length > 0) {
                            $("input[name=lat_s]").val(results[0].geometry.location.lat);
                            $("input[name=long_s]").val(results[0].geometry.location.lng);
                            marker.setPosition(results[0].geometry.location);
                            map.setCenter(results[0].geometry.location);
                        }

                    }
                });

            }
        }
    });
    map.controls[google.maps.ControlPosition.TOP_LEFT].push(searchInput);

}


